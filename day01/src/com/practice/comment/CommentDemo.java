package com.practice.comment;

/** 文档注释
 这是一个用来讲解注释的使用的
 */
public class CommentDemo {
    public static void main(String[] args) {
        // 单行注释
        // 打印2行诗句
        System.out.println("我欲乘风归去，又恐琼楼玉宇");

        /*
          多行注释
          打印3行诗句，依次打印
         */
        System.out.println("我欲乘风归去，又恐琼楼玉宇");
    }
}
